class AdvancedMath implements AdvancedOperations{
    public void pow(int a, int b){
        System.out.println("============================");
        System.out.println("Power");
        System.out.println(Math.pow(a, b));
        System.out.println("============================");
    }
    public void sqrt(int a){
        System.out.println("============================");
        System.out.println("Square Root");
        System.out.println(Math.sqrt(a));
        System.out.println("============================");
    }
    public void log(int a){
        System.out.println("============================");
        System.out.println("Logarithm");
        System.out.println(Math.log(a));
        System.out.println("============================");
    }
    public void ln(int a){
        System.out.println("============================");
        System.out.println("Natural Logarithm");
        System.out.println(Math.log(a));
        System.out.println("============================");
    }
}
